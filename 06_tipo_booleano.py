# Operações Binárias

# Ou (or) -> um ou outro tem que ser verdadeiro para o resultado ser verdadeiro
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# E (and) -> ambos os valores devem ser verdadeiro
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("*****************")
# 5 > 6 é uma operação booleana
print(5 > 6)
print(5 < 6)
print(6 == 6)
print(4 <= 6)
